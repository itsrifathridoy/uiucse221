// Program that will declare a variable from each data type: double, boolean. Then it will
// initialize them with values and print them.
#include <stdio.h>
int main(){
    double a;
    bool b;
    a = 3.14;
    b=true;
    printf("The double value: %.6lf\n",a);
    printf("The boolean value: %d\n",b);
    return 0;
}
